#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
			i++;
		if (!(str = (char*)ft_memalloc(i)))
			return (NULL);
		i = 0;
		while (s[i] != '\0')
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (&str[0]);
	}
	else
		return (NULL);
}
