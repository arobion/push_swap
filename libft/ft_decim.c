#include "libft.h"

int		ft_decim(char *str)
{
	int		nb;
	int		i;

	i = 0;
	nb = 0;
	while (str[i] != '\0')
	{
		nb = nb * 2 + str[i] - '0';
		i++;
	}
	return (nb);
}
