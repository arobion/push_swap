#include "libft.h"

size_t	ft_strlen_at_p(const char *s, int c)
{
	unsigned int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0' && s[i] != (char)c)
			i++;
		return ((size_t)i);
	}
	return (0);
}
